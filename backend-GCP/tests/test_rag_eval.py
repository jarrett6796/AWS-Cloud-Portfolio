import importlib.util
import json
import tempfile
import unittest
from pathlib import Path


SCRIPT_PATH = Path(__file__).resolve().parents[1] / "scripts" / "evaluate_rag.py"
SPEC = importlib.util.spec_from_file_location("evaluate_rag", SCRIPT_PATH)
evaluate_rag = importlib.util.module_from_spec(SPEC)
SPEC.loader.exec_module(evaluate_rag)


class RagEvalTest(unittest.TestCase):
    def test_required_terms_check_reports_missing_terms(self):
        passed, missing = evaluate_rag.contains_all_terms(
            "Cloud Run hosts the FastAPI backend.",
            ["Cloud Run", "Firestore"],
        )

        self.assertFalse(passed)
        self.assertEqual(missing, ["Firestore"])

    def test_forbidden_terms_check_reports_found_terms(self):
        passed, found = evaluate_rag.contains_no_forbidden_terms(
            "The current answer is generated by Bedrock.",
            ["Bedrock"],
        )

        self.assertFalse(passed)
        self.assertEqual(found, ["Bedrock"])

    def test_source_match_check_uses_expected_source_files(self):
        passed, missing, returned = evaluate_rag.check_source_match(
            [{"file_name": "CAPSTONE_PROJECT_STATE.md"}],
            ["CAPSTONE_PROJECT_STATE.md", "GCP_RAG_PROJECT_STATE.md"],
        )

        self.assertFalse(passed)
        self.assertEqual(missing, ["GCP_RAG_PROJECT_STATE.md"])
        self.assertEqual(returned, ["CAPSTONE_PROJECT_STATE.md"])

    def test_citation_grounding_requires_valid_source_ids(self):
        passed, cited, valid = evaluate_rag.check_citation_grounding(
            "The backend uses Cloud Run. [S1]",
            [{"source_id": "S1"}],
        )

        self.assertTrue(passed)
        self.assertEqual(cited, ["S1"])
        self.assertEqual(valid, ["S1"])

    def test_citation_grounding_fails_missing_citation(self):
        passed, cited, valid = evaluate_rag.check_citation_grounding(
            "The backend uses Cloud Run.",
            [{"source_id": "S1"}],
        )

        self.assertFalse(passed)
        self.assertEqual(cited, [])
        self.assertEqual(valid, ["S1"])

    def test_extract_cited_source_ids_handles_single_bracket(self):
        self.assertEqual(
            evaluate_rag.extract_cited_source_ids("Cloud Run hosts this [S1]."),
            ["S1"],
        )

    def test_extract_cited_source_ids_handles_grouped_bracket(self):
        self.assertEqual(
            evaluate_rag.extract_cited_source_ids("Cloud Run hosts this [S1, S2]."),
            ["S1", "S2"],
        )

    def test_extract_cited_source_ids_handles_grouped_bracket_no_spaces(self):
        self.assertEqual(
            evaluate_rag.extract_cited_source_ids("Cloud Run hosts this [S1,S2,S3]."),
            ["S1", "S2", "S3"],
        )

    def test_extract_cited_source_ids_handles_multiple_bracket_groups(self):
        self.assertEqual(
            evaluate_rag.extract_cited_source_ids(
                "First point [S1, S2]. Second point [S3]."
            ),
            ["S1", "S2", "S3"],
        )

    def test_citation_grounding_passes_grouped_citation(self):
        passed, cited, valid = evaluate_rag.check_citation_grounding(
            "The backend uses Cloud Run and FastAPI [S1, S2].",
            [{"source_id": "S1"}, {"source_id": "S2"}],
        )

        self.assertTrue(passed)
        self.assertEqual(cited, ["S1", "S2"])
        self.assertEqual(valid, ["S1", "S2"])

    def test_citation_grounding_still_fails_invalid_grouped_citation(self):
        passed, cited, valid = evaluate_rag.check_citation_grounding(
            "The backend uses Cloud Run [S1, S9].",
            [{"source_id": "S1"}],
        )

        self.assertFalse(passed)
        self.assertEqual(cited, ["S1", "S9"])
        self.assertEqual(valid, ["S1"])

    def test_no_answer_check_matches_expected_no_answer(self):
        passed, actual_no_answer = evaluate_rag.check_no_answer(
            "I do not know based on the indexed project documents.",
            expect_no_answer=True,
        )

        self.assertTrue(passed)
        self.assertTrue(actual_no_answer)

    def test_evaluate_response_builds_failure_categories(self):
        result = evaluate_rag.evaluate_response(
            {
                "id": "case-1",
                "category": "architecture",
                "question": "What hosts the backend?",
                "expected_sources": ["CAPSTONE_PROJECT_STATE.md"],
                "expected_doc_types": ["state"],
                "answer_must_include": ["Cloud Run"],
                "answer_should_not_include": ["Bedrock"],
                "expect_no_answer": False,
            },
            {
                "answer": "Bedrock hosts it.",
                "sources": [{"file_name": "OTHER.md", "source_id": "S1"}],
            },
            200,
            100,
        )

        self.assertFalse(result["passed"])
        self.assertIn("source_mismatch", result["failure_reasons"])
        self.assertIn("doc_type_mismatch", result["failure_reasons"])
        self.assertIn("missing_required_terms", result["failure_reasons"])
        self.assertIn("forbidden_claim", result["failure_reasons"])
        self.assertIn("missing_citation", result["failure_reasons"])

    def test_doc_type_mismatch_is_separate_from_source_mismatch(self):
        result = evaluate_rag.evaluate_response(
            {
                "id": "case-1",
                "category": "architecture",
                "question": "What hosts the backend?",
                "expected_sources": ["CAPSTONE_PROJECT_STATE.md"],
                "expected_doc_types": ["architecture"],
                "answer_must_include": ["Cloud Run"],
                "answer_should_not_include": [],
                "expect_no_answer": False,
            },
            {
                "answer": "Cloud Run hosts it. [S1]",
                "sources": [
                    {
                        "file_name": "CAPSTONE_PROJECT_STATE.md",
                        "doc_type": "state",
                        "source_id": "S1",
                    }
                ],
            },
            200,
            100,
        )

        self.assertFalse(result["passed"])
        self.assertNotIn("source_mismatch", result["failure_reasons"])
        self.assertIn("doc_type_mismatch", result["failure_reasons"])

    def test_summary_includes_doc_type_match_rate(self):
        summary = evaluate_rag.summarize_results(
            [
                {
                    "passed": False,
                    "latency_ms": 100,
                    "failure_reasons": ["doc_type_mismatch"],
                    "checks": {
                        "source_match": True,
                        "doc_type_match": False,
                        "required_terms": True,
                        "forbidden_terms": True,
                        "citation_grounding": True,
                        "no_answer": True,
                    },
                }
            ]
        )

        self.assertEqual(summary["source_match_rate"], 1.0)
        self.assertEqual(summary["doc_type_match_rate"], 0.0)

    def test_threshold_pass_and_fail_behavior(self):
        summary = {
            "overall_pass_rate": 0.79,
            "source_match_rate": 0.75,
            "citation_grounding_rate": 0.91,
            "average_latency_ms": 500,
        }
        thresholds = {
            "min_overall_pass_rate": 0.80,
            "min_source_match_rate": 0.75,
            "min_citation_rate": 0.90,
            "max_average_latency_ms": 12000,
        }

        result = evaluate_rag.evaluate_thresholds(summary, thresholds)

        self.assertFalse(result["passed"])
        self.assertEqual(result["failures"], ["overall_pass_rate"])

    def test_json_and_markdown_report_generation(self):
        report = evaluate_rag.build_report(
            base_url="http://localhost:8080",
            questions_path="evals/golden_questions.json",
            results=[
                {
                    "id": "case-1",
                    "category": "architecture",
                    "question": "What hosts the backend?",
                    "passed": True,
                    "failure_reasons": [],
                    "latency_ms": 100,
                    "sources_returned": ["CAPSTONE_PROJECT_STATE.md"],
                    "expected_sources": ["CAPSTONE_PROJECT_STATE.md"],
                    "answer_excerpt": "Cloud Run hosts it. [S1]",
                    "checks": {
                        "source_match": True,
                        "doc_type_match": True,
                        "required_terms": True,
                        "forbidden_terms": True,
                        "citation_grounding": True,
                        "no_answer": True,
                    },
                }
            ],
            thresholds={
                "min_overall_pass_rate": 0.80,
                "min_source_match_rate": 0.75,
                "min_citation_rate": 0.90,
                "max_average_latency_ms": 12000,
            },
            soft_fail=True,
        )

        with tempfile.TemporaryDirectory() as temp_dir:
            markdown_path = Path(temp_dir) / "report.md"
            json_path = Path(temp_dir) / "report.json"
            evaluate_rag.save_reports(report, markdown_path, json_path)

            self.assertIn("# RAG Evaluation Report", markdown_path.read_text())
            loaded = json.loads(json_path.read_text())
            self.assertEqual(loaded["summary"]["total_cases"], 1)


if __name__ == "__main__":
    unittest.main()
